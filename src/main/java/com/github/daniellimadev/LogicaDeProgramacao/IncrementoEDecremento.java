package com.github.daniellimadev.LogicaDeProgramacao;

public class IncrementoEDecremento {
    public static void main(String[] args) {
        // Usando "++" para incrementar 1 e "--" para decremetar 1 em valores inteiros (int e log).

        int posicao = 20;
        posicao++;
        posicao++;
        posicao--;
        posicao++;

        System.out.println("Posicao: " + posicao);
    }
}
